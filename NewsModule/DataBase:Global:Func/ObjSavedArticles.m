#import "ObjSavedArticles.h"


@implementation ObjSavedArticles

@synthesize title = _title;
@synthesize subTitle = _subTitle;
@synthesize articleTxt = _articleTxt;
@synthesize author = _author;
@synthesize date = _date;
@synthesize articleType = _articleType;
@synthesize imageURL = _imageURL;
@synthesize articleID = _articleID;
@synthesize thumbID = _thumbID;
@synthesize videoURL =_videoURL;
@synthesize articleURL =_articleURL;



-(void)dealloc{
	self.title = nil;
	self.subTitle = nil;
	self.articleTxt = nil;
	self.author = nil;
	self.date = nil;
	self.articleType = nil;
	self.imageURL=nil;
	self.articleID=nil;
	self.thumbID=nil;
	self.videoURL=nil;
	self.articleURL=nil;
	[super dealloc];
}

@end
